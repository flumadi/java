public class AddTwoNumbers {
    public static void main(String[] args) {
        int A = 2; // Initialize A with 2
        int B = 3; // Initialize B with 3
        int C = A + B; // Add A and B to get C

        System.out.println("The sum of " + A + " and " + B + " is " + C); // Print the result
    }
}