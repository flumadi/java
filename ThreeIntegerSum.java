public class ThreeIntegerSum {
    public static void main(String[] args) {
        int A = 2; // Initialize A with 2
        int B = 3; // Initialize B with 3
        int C = 4; // Initialize C with 4
        int D = A + B + C; // Add A, B and C to get D

        System.out.print("The sum of these A, B and C become " + D); // Print the result
    }
}